public class Member {
	private String Id;
	private String password;
	private String name;
	private int birth;
	private int phone;

	public String getId() {
		return Id;
	}

	public void setId(String Id) {
		this.Id = Id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBirth() {
		return birth;
	}

	public void setBirth() {
		this.birth = birth;
	}

	public int getPhone() {
		return phone;
	}

	public void setPhone() {
		this.phone = phone;
	}

}
